// for 迴圈：一個有「起點」、「終點」和「步伐」的計數器。
// 用途：當我知道「具體要跑幾次」時使用。

//  (1. 起點)    (2. 終點/檢查站)  (3. 步伐/改變)
for (let i = 1; i <= 10; i++) {
    // (4. 任務)
    console.log(i);
}

// 執行順序：
// 順序,行為 (Action),備註 (The Rule)
// 1,初始化 (Init: let i = 1),只跑一次。 永遠不會再跑第二次。
// 2,檢查條件 (Condition: i <= 10),每次循環的閘門。 判斷是否要進去工作。
// 3,執行程式碼 (console.log(i)),如果閘門打開，就執行裡面的任務。
// 4,邁出步伐 (Step: i++),任務完成後，推進計數器。
// 5,回到步驟 2,嚴格重複檢查，直到條件失敗。

// 回顧
// 記住這個流程，它是一個嚴格的工廠流水線，不會出錯：
// 第一個設定（初始化）：只執行 一次。它設置了你的計數器從哪裡開始。
// 第二個設定（條件檢查）：執行初始化後，電腦會立即檢查這個條件。這是決定「是否能開始工作」的閘門。
// 執行迴圈體（程式碼區塊）：如果閘門（條件）打開，才執行大括號 {} 裡面的所有程式碼。
// 第三個設定（迭代/步伐）：執行完工作後，電腦才會按照你的設定，走一步（或跳幾步），改變計數器的狀態。

